package org.lession2_homework;

public class Calculator {
    public static int sum(int a, int b) {
        return a + b;
    }
    public static long sum(long a, long b) {
        return a + b;
    }
    public static double sum(double a, double b) {
        return a + b;
    }
    public static int mult(int a, int b) {
        return a * b;
    }
    public static long mult(long a, long b) {
        return a * b;
    }
    public static double mult(double a, double b) {
        return a * b;
    }
    public static double div(int a, int b) { return (double)(a)/b; }
    public static double div(long a, long b) { return (double)(a)/b; }
    public static double div(double a, double b) { return a/b; }
    public static int subtr(int a, int b) {
        return a - b;
    }
    public static long subtr(long a, long b) {
        return a - b;
    }
    public static double subtr(double a, double b) {
        return a - b;
    }
}
